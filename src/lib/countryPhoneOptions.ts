export const getCountryPhoneOption = (countryCode: string) =>
  COUNTRY_PHONE_OPTIONS.find((option) => option.value === countryCode) || COUNTRY_PHONE_OPTIONS[0];

export const getCountryPhoneError = (phoneNumber: string, selectedCountryPhone: CountryPhoneOption) => {
  const trimmedPhone = phoneNumber.trim();
  if (!trimmedPhone) return '';

  const digitsOnly = trimmedPhone.replace(/\D/g, '');
  if (digitsOnly.length < selectedCountryPhone.minDigits || digitsOnly.length > selectedCountryPhone.maxDigits) {
    return selectedCountryPhone.minDigits === selectedCountryPhone.maxDigits
      ? `Phone number must be ${selectedCountryPhone.maxDigits} digits for ${selectedCountryPhone.label}.`
      : `Phone number must be ${selectedCountryPhone.minDigits}-${selectedCountryPhone.maxDigits} digits for ${selectedCountryPhone.label}.`;
  }

  return '';
};

export const normalizePhoneDigits = (phoneNumber: string, selectedCountryPhone: CountryPhoneOption) =>
  phoneNumber.replace(/\D/g, '').slice(0, selectedCountryPhone.maxDigits);
